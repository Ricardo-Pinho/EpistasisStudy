#!/bin/bash

format1="$2/BOOST"
format2="$2/TEAM"
format3="$2/SNPR"
format4="$2/BEAM3"
format5="$2/SNPH"

result1="$3/BOOST"
result2="$3/TEAM"
result3="$3/SNPR"
result4="$3/BEAM3"
result5="$3/SNPH"

echo "//////////////////////////////////////////////////"
echo "BOOST"
echo "$1"
echo "//////////////////////////////////////////////////"

cd ./BOOST; 
bash BOOSTformat.sh $1 $format1 $result1;
bash BOOSTconfig.sh $1 $result1 $format1;

echo "//////////////////////////////////////////////////"
echo "TEAM"
echo "$1"
echo "//////////////////////////////////////////////////"

cd ../TEAM;
bash TEAMformat.sh $1 $format2 $result2;
bash TEAMconfig.sh $1 $result2 $format2;
echo "//////////////////////////////////////////////////"
echo "//////////////////////////////////////////////////"
echo "SNPR"
echo "$1"
echo "//////////////////////////////////////////////////"
echo "//////////////////////////////////////////////////"
cd ../SNPR;
bash SNPRulerformat.sh $1 $format3 $result3;
bash SNPRulerconfig.sh $1 $result3 $format3;
echo "//////////////////////////////////////////////////"
echo "//////////////////////////////////////////////////"
echo "BEAM3"
echo "$1"
echo "//////////////////////////////////////////////////"
echo "//////////////////////////////////////////////////"
cd ../BEAM3;
bash BEAMformat.sh $1 $format4 $result4;
bash BEAMconfig.sh $1 $result4 $format4;
echo "//////////////////////////////////////////////////"
echo "//////////////////////////////////////////////////"
echo "SNPH"
echo "$1"
echo "//////////////////////////////////////////////////"
echo "//////////////////////////////////////////////////"
cd ../SNPH;
bash SNPHarvesterformat.sh $1 $format5 $result5;
bash SNPHarvesterconfig.sh $1 $result5 $format5;

echo "//////////////////////////////////////////////////"
echo "//////////////////////////////////////////////////"
echo "Ensemble"
echo "$1"
echo "//////////////////////////////////////////////////"
echo "//////////////////////////////////////////////////"

cd ..
bash Ensembleepistasis.sh $1 $result1 $result2 $result3 $4;
bash Ensemblemaineffect.sh $1 $result1 $result4 $result5 $4;